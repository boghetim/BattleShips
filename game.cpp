void game::setShips()
{
    string tempName;
    int enemyType=5;

    cout << "Hello Player, what is your battleships name?" << endl;
    cin >> tempName;

    shipName.setName(tempName);

    cout << "what type of enemy do you want ? press '1' for slow and '2' for fast "<< endl;
    cin >> enemyType;


    if (enemyType ==1)
    {
        slow.setDiff(enemyType);
    }
    if (enemyType ==2)
    {
        fast.setDiff(enemyType);
    }


}

